/*Java scrip: switch*/
let dia = "domingo";
switch (dia){
    case "lunes":
        console.log("Laboral");
        break;
    case "martess":
        console.log("Martes navidad");
        break;
    case "miercoles":
        console.log("miércoles halloween");
        break;
    case "jueves":
        console.log("Laboral");
        break;
    case "viernes":
        console.log("Viernes Santo");
        break;
    case "sabado":
        console.log("Sábado de gloria");
        break;
    case "domingo":
        console.log("No Laboral");
        break;
    default:
        console.log("Día no válido");
}